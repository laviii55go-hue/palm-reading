export function calcAnimalIndices(
  year: number, month: number, day: number
): { animalIndex: number; subtypeIndex: number } {
  // ユリウス通算日をベースにした計算（0〜59の一様分布）
  const a = Math.floor((14 - month) / 12);
  const y = year - a;
  const m = month + 12 * a - 2;
  const jd =
    day +
    Math.floor((153 * m + 2) / 5) +
    365 * y +
    Math.floor(y / 4) -
    Math.floor(y / 100) +
    Math.floor(y / 400);
  const idx = ((jd % 60) + 60) % 60;
  return { animalIndex: idx % 12, subtypeIndex: Math.floor(idx / 12) };
}

