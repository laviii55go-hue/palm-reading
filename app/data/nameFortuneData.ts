// 81を超える画数は 81 に還元（81画＝1画と同等）
export function normalizeStroke(n: number): number {
  if (n < 1) return 1;
  if (n > 81) {
    const r = ((n - 1) % 81) + 1;
    return r;
  }
  return n;
}

